    private void BtnHitungActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnHitungActionPerformed
        // TODO add your handling code here:
        try {
            int tahun = Integer.parseInt(Tahun.getText());
            String bulan = Bulan.getSelectedItem().toString();
            MenghitungJumlahHari hitungHari = new MenghitungJumlahHari();
            HasilAkhir.setText("Jumlah hari pada bulan " + Bulan + " tahun " + Tahun + " adalah " + getHari);
            HasilAkhir.setText(getHari);
        } catch (NumberFormatException e) {
            HasilAkhir.setText("Invalid");
        }
    }//GEN-LAST:event_BtnHitungActionPerformed

