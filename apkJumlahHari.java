    private void BtnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnSimpanActionPerformed
        // TODO add your handling code here:
        prosesSimpan();
         try (BufferedWriter writer = new BufferedWriter(new FileWriter("hasil_perhitungan.txt", true))) {
            writer.write("Tahun: " + Tahun.getText() + ", Bulan: " + Bulan.getSelectedItem().toString() + ", Hasil: " + HasilAkhir.getText());
            writer.newLine();
            writer.flush();
            HasilAkhir.setText("Hasil perhitungan disimpan.");
        } catch (IOException e) {
            HasilAkhir.setText("Gagal menyimpan hasil.");}
    }//GEN-LAST:event_BtnSimpanActionPerformed

